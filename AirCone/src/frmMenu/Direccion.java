public void ListarDatos(){
direccion=db.ListDireccion();
DefaultTableModel tb=(DefaultTableModel)tblTabla.getModel();
for(ClasesPrincipales.Direcciones dr: direccion){
tb.addRow(new Object[]{dr.getCasa_id(),dr.getProvincia(),dr.getCanton(),dr.getDistrito(),dr.getCodigo_postal()});
}
}
public void LimpiarFormulario(){
    DefaultTableModel tb=(DefaultTableModel)tblTabla.getModel();
    for(int i=tb.getRowCount()-1;i>=0;i--)
        tb.removeRow(i);
}


