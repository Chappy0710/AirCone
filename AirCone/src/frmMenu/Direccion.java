    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Direcciones dr = new Direcciones();
    
        dr.setCasa_id(Integer.parseInt(txtcasa_id.getText()));
        dr.setProvincia(txtprovincia.getText());
        dr.setCanton(txtcanton.getText());
        dr.setDistrito(txtdistrito.getText());
        dr.setCodigo_postal(Integer.parseInt(txtcodigo_postal.getText()));
        if(!"".equals(txtcasa_id.getText())&&!"".equals(txtprovincia.getText())&&!"".equals(txtcanton.getText())&&!"".equals(txtdistrito.getText())&&!"".equals(txtcodigo_postal.getText())){
            JOptionPane.showMessageDialog(this, "Datos Ingresados correctamente","",JOptionPane.INFORMATION_MESSAGE);
            db.insertarDirecciones(dr);
            LimpiarFormulario();
            ListarDatos();
        }else{
            JOptionPane.showMessageDialog(this, "Falta Ingresar Datos", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

